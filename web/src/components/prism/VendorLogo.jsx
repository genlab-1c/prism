// Данные знака вендора для отрисовки в чужом <svg> (напр. выгружаемый чарт лидерборда),
// где компонент VendorLogo с его <span>/CSS-переменными не годится: возвращаем голые пути
// (24×24), цвет и режим. color может быть 'var(--ink-100)' — вызывающий подставит свой ink.
export function vendorGlyph(vendor) {
  const icon = ICONS[vendor];
  const custom = !icon && CUSTOM[vendor];
  if (!icon && !custom) return null; // неизвестный вендор → монограмма на стороне вызова
  return {
    paths: icon ? [icon.path] : custom.paths,
    color: icon ? `#${icon.hex}` : custom.color,
    stroke: !!custom && custom.mode === 'stroke',
    title: icon ? icon.title : custom.title,
  };
}

export function VendorLogo({ vendor, name = '?', size = 32, style = {} }) {
  const icon = ICONS[vendor];
  const custom = !icon && CUSTOM[vendor];
  if (!icon && !custom) return <Avatar name={name} size={size} style={style} />; // неизвестный вендор

  const title = icon ? icon.title : custom.title;
  const color = icon ? `#${icon.hex}` : custom.color;
  const stroke = !!custom && custom.mode === 'stroke';
  const paths = icon ? [icon.path] : custom.paths;
  const bg = color.startsWith('#') ? `${color}1f` : `color-mix(in srgb, ${color} 14%, transparent)`;
  const g = Math.round(size * 0.56);
  return (
    <span
      title={title}
      style={{
        width: size, height: size, flex: 'none', display: 'inline-grid', placeItems: 'center',
        borderRadius: 'var(--radius-md)', background: bg, border: '1px solid var(--line)', ...style,
      }}>
      <svg
        width={g} height={g} viewBox="0 0 24 24" aria-hidden="true"
        fill={stroke ? 'none' : color}
        stroke={stroke ? color : undefined}
        strokeWidth={stroke ? 2.3 : undefined}
        strokeLinecap="round" strokeLinejoin="round">
        {paths.map((d, i) => <path key={i} d={d} />)}
      </svg>
    </span>
  );
}
