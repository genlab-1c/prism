"""Учёт стоимости встроенных команд для исполнительной оценки O (категория A).

Счётчик oscript -codestat видит только операторы BSL, написанные кандидатом, и слеп к
работе ВНУТРИ встроенных команд (Массив.Найти — линейный перебор; склейка строк `+` —
копирование неизменяемой строки). Без учёта медленное O(n²)-решение через Найти/склейку
выглядит линейным. Приём: на ЗАМЕРОЧНОМ прогоне (не на проверке корректности!) подменяем
такие места на инструментированные BSL-аналоги, которые делают ту же работу циклом —
и счётчик её видит. Семантика сохранена.

Подмена идёт по ТОКЕНАМ, а не по сырому тексту: токенизатор BSL отделяет строковые
литералы и комментарии, поэтому `Найти` или `X = X + ...` внутри строки/комментария НЕ
трогаются (главная хрупкость регэкспов). Это «разбор кода» в объёме, достаточном для
структурных подмен ниже; полный AST не нужен.

Покрытые встроенные (остальные счётчик видит как 1 операцию — расширять по мере задач,
доказывая нужду якорем perf_baseline.bsl в prism check):
  · Найти / НайтиСтроки — линейный перебор коллекции/таблицы;
  · Вставить / Удалить  — сдвиг хвоста массива O(n) (у Соответствия/Структуры — O(1), не цена);
  · X = X + …            — склейка строк (копирование по длине).
"""

from __future__ import annotations

import re

# Инструментированные аналоги встроенных команд.
HELPERS = """Функция ПризмаНайти(Кол, Иск)
    Сч = 0;
    Для Каждого Э Из Кол Цикл
        Если Э = Иск Тогда
            Возврат Сч;
        КонецЕсли;
        Сч = Сч + 1;
    КонецЦикла;
    Возврат Неопределено;
КонецФункции

Функция ПризмаНайтиСтроки(Таблица, Отбор)
    Найдено = Новый Массив;
    Для Каждого Стр Из Таблица Цикл
        Подходит = Истина;
        Для Каждого КлючЗначение Из Отбор Цикл
            Если Стр[КлючЗначение.Ключ] <> КлючЗначение.Значение Тогда
                Подходит = Ложь;
            КонецЕсли;
        КонецЦикла;
        Если Подходит Тогда
            Найдено.Добавить(Стр);
        КонецЕсли;
    КонецЦикла;
    Возврат Найдено;
КонецФункции

Функция ПризмаСклейка(Лев, Прав)
    Если ТипЗнч(Лев) = Тип("Строка") Тогда
        Длина = СтрДлина(Лев);
        Сч = 0;
        Пока Сч < Длина Цикл
            Сч = Сч + 1;
        КонецЦикла;
    КонецЕсли;
    Возврат Лев + Прав;
КонецФункции

Процедура ПризмаВставить(Кол, Поз, Значение)
    Если ТипЗнч(Кол) = Тип("Массив") Тогда
        Сч = Поз;
        Пока Сч < Кол.Количество() Цикл
            Сч = Сч + 1;
        КонецЦикла;
    КонецЕсли;
    Кол.Вставить(Поз, Значение);
КонецПроцедуры

Процедура ПризмаУдалить(Кол, Индекс)
    Если ТипЗнч(Кол) = Тип("Массив") Тогда
        Сч = Индекс;
        Пока Сч < Кол.Количество() Цикл
            Сч = Сч + 1;
        КонецЦикла;
    КонецЕсли;
    Кол.Удалить(Индекс);
КонецПроцедуры"""

# Токены BSL. Порядок важен: строка/комментарий ловятся раньше пунктуации.
# Строка 1С: "..." с удвоением кавычки ("") как экранированием; может быть многострочной.
_TOKEN_RE = re.compile(
    r"(?P<ws>\s+)"
    r"|(?P<comment>//[^\n]*)"
    r'|(?P<string>"(?:[^"]|"")*")'
    r"|(?P<ident>[A-Za-zА-Яа-яЁё_][A-Za-zА-Яа-яЁё0-9_]*)"
    r"|(?P<number>\d+\.?\d*)"
    r"|(?P<punct>[().,;+\-*/=<>%\[\]])"
    r"|(?P<other>.)"
)

# Встроенные методы со скрытой стоимостью → инструментированный аналог (см. HELPERS).
# Найти/НайтиСтроки — линейный перебор; Вставить/Удалить — сдвиг хвоста массива O(n).
# Аналоги Вставить/Удалить начисляют стоимость ТОЛЬКО массиву (по ТипЗнч): у Соответствия
# и Структуры одноимённые методы за O(1), их аналог просто делегирует без цены.
_METHOD_ANALOGS = {
    "Найти": "ПризмаНайти",
    "НайтиСтроки": "ПризмаНайтиСтроки",
    "Вставить": "ПризмаВставить",
    "Удалить": "ПризмаУдалить",
}

# Значимые границы начала оператора (после них идёт новый оператор).
_STMT_START = {";", "Тогда", "Цикл", "Иначе", "Попытка", "Исключение"}
_TRIVIA = {"ws", "comment"}


class _Tok:
    __slots__ = ("kind", "text")

    def __init__(self, kind: str, text: str):
        self.kind, self.text = kind, text


def _tokenize(code: str) -> list[_Tok]:
    return [_Tok(m.lastgroup, m.group()) for m in _TOKEN_RE.finditer(code)]


def instrument(code: str) -> str:
    """Подменяет встроенные операции со скрытой стоимостью на инструментированные аналоги.

    - `X.Найти(Y)`   → `ПризмаНайти(X, Y)`           (линейный перебор коллекции)
    - `X = X + ...;` → `X = ПризмаСклейка(X, ...);`  (склейка; аналог проверяет тип на лету,
      числам стоимость не начисляется, поэтому `Счётчик = Счётчик + 1` безопасен)

    Подмены не срабатывают внутри строк и комментариев (они — отдельные токены).
    """
    toks = _tokenize(code)
    sig = [i for i, t in enumerate(toks) if t.kind not in _TRIVIA]  # индексы значимых токенов

    def s(k: int) -> _Tok:  # значимый токен по порядковому номеру k
        return toks[sig[k]]

    edits: list[tuple[int, int, list[_Tok]]] = []  # (real_start, real_end_excl, замена)

    k = 0
    while k < len(sig):
        t = s(k)
        # --- X.Найти( / X.НайтиСтроки( → Призма…(X, ---
        if (
            t.kind == "ident"
            and k + 3 < len(sig)
            and s(k + 1).text == "."
            and s(k + 2).text in _METHOD_ANALOGS
            and s(k + 3).text == "("
        ):
            edits.append(
                (
                    sig[k],
                    sig[k + 3] + 1,
                    [
                        _Tok("ident", _METHOD_ANALOGS[s(k + 2).text]),
                        _Tok("punct", "("),
                        _Tok("ident", t.text),
                        _Tok("punct", ","),
                        _Tok("ws", " "),
                    ],
                )
            )
            k += 4
            continue
        # --- начало оператора: A = A + ... ; → A = ПризмаСклейка(A, ... ); ---
        at_stmt_start = (k == 0) or (s(k - 1).text in _STMT_START)
        if (
            at_stmt_start
            and t.kind == "ident"
            and k + 3 < len(sig)
            and s(k + 1).text == "="
            and s(k + 2).text == t.text
            and s(k + 3).text == "+"
        ):
            # найти ';' этого оператора (на значимом уровне; ';' внутри строки — отдельный токен строки)
            j = k + 4
            while j < len(sig) and s(j).text != ";":
                j += 1
            if j < len(sig):
                # заменить второй `A` и `+` (sig k+2..k+3) на `ПризмаСклейка ( A ,`
                # (пробел после запятой не добавляем — уцелеет ws, что шёл после `+`)
                edits.append(
                    (
                        sig[k + 2],
                        sig[k + 3] + 1,
                        [
                            _Tok("ident", "ПризмаСклейка"),
                            _Tok("punct", "("),
                            _Tok("ident", t.text),
                            _Tok("punct", ","),
                        ],
                    )
                )
                # вставить `)` перед `;`
                edits.append((sig[j], sig[j], [_Tok("punct", ")")]))
                k = j + 1
                continue
        k += 1

    for start, end, repl in sorted(edits, key=lambda e: e[0], reverse=True):  # справа налево
        toks[start:end] = repl
    return "".join(t.text for t in toks)
